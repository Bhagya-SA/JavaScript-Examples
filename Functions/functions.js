// Normal function in JavaScript

// Function to greet a person
function greet(name) {
    return "Hello, " + name + "!";
}

// Function to add two numbers
function add(a, b) {
    return a + b;
}

// Using the functions
console.log(greet("Bhagya"));          // Hello, Bhagya!
console.log("Sum:", add(10, 5));       // Sum: 15
