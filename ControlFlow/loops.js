// Loops in JavaScript

// For loop
console.log("For loop:");
for (let i = 1; i <= 5; i++) {
    console.log(i);
}

// While loop
console.log("\nWhile loop:");
let count = 3;
while (count > 0) {
    console.log(count);
    count--;
}

// Do-While loop
console.log("\nDo-While loop:");
let num = 1;
do {
    console.log(num);
    num++;
} while (num <= 3);
