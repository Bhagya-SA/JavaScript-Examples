// JavaScript Data Types

// Number
let age = 21;
console.log("Age:", age, "- Type:", typeof age);

// String
let name = "Bhagya";
console.log("Name:", name, "- Type:", typeof name);

// Boolean
let isStudent = true;
console.log("Is Student:", isStudent, "- Type:", typeof isStudent);

// Undefined
let city;
console.log("City:", city, "- Type:", typeof city);

// Null
let job = null;
console.log("Job:", job, "- Type:", typeof job);

// Object
let student = { name: "Bhagya", age: 21 };
console.log("Student:", student, "- Type:", typeof student);
